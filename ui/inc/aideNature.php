          <p class="message-title">Comment lire ces graphiques ?</p>
          <p>marché Les marchés publics sont répartis en quatre grandes natures de contrats : </p>
          <ul>
            <li>Marché : cas général, contrat entre un acheteur public et un fournisseur</li>
            <li>Marché de partenariat : contrat spécifique qui « permet de confier à un opérateur économique ou à un groupement d’opérateurs économiques une mission globale » sous maîtrise d’ouvrage privée.</li>
            <li>Accord-cadre : contrat spécifique ayant pour objet d’établir les termes régissant les marchés (dits subséquents) à passer au cours d’une période donnée, en particulier en ce qui concerne les prix et, s’il y a lieu, les quantités envisagées.</li>
            <li>Marché subséquent : contrat spécifique rattaché à un accord-cadre</li>
          </ul>
